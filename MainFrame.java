    private void ScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleButtonActionPerformed
        String Code = (String)CourseBox1.getSelectedItem();
        String ID = (String)IDBox1.getSelectedItem();
        Timestamp time = new Timestamp(System.currentTimeMillis());
        String status;
        
        int takenSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, Code);
        int totalSeats  = CourseQueries.getCourseSeats(currentSemester, Code);

        
        if (takenSeats >= totalSeats){
            status = "W";
        }
        else{
            status = "S";
        }
        
        ScheduleEntry newEntry = new ScheduleEntry(currentSemester, Code, ID, status, time);
        ScheduleQueries.addScheduleEntry(newEntry);
        
        scheduleLabel.setText("Course " + Code + " has been scheduled, status is: " + status);
    }//GEN-LAST:event_ScheduleButtonActionPerformed

