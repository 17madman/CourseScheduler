    private void ShowScheduleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowScheduleButton2ActionPerformed
        String id = (String)IDBox5.getSelectedItem();
        StudentEntry bye = StudentQueries.getStudent(id);
        String byeID = bye.getStudentID();
        StudentQueries.dropStudent(byeID);
        String courseCode;
        ArrayList<ScheduleEntry> entriesToDrop = ScheduleQueries.getScheduleByStudent(currentSemester, id);
        
        String returnString = "";
        String firstFinal;
        
        for (ScheduleEntry x : entriesToDrop){
            courseCode = x.getCourseCode();
            ArrayList<ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
            if (waitlist.size() > 0){
                ScheduleEntry first = waitlist.get(0);
                String firstName = first.getStudentID();
                firstFinal = " for dropped student. Switching first on waitlist to scheduled, this person is: "+firstName;
            }
            else{
                firstFinal = " for dropped student. Waitlist empty, so no need to move up.";
            }
            
            returnString = returnString + "Dropping Course: " + courseCode + firstFinal +"\n";
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, id, courseCode);
        }
        
        ScheduleTitle2.setText("Dropping Student: "+id+" and removing the following schedule entries:");
        
        ScheduleDisplay2.setText(returnString);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_ShowScheduleButton2ActionPerformed

